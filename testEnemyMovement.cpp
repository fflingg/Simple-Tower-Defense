void viewGrid(vector<vector<int>> map, Enemy enemy)
{
    for (int i = 0; i < gridRow; ++i)
    {
        for (int j = 0; j < gridColumn; ++j)
        {
            if (i == enemy.row - 1 && j == enemy.col - 1)
            {
                cout << "e ";
            }
            else if (map[i][j] == 0)
            {
                cout << "- ";
            }
            else if (map[i][j] == 1)
            {
                cout << "# ";
            }
            else if (map[i][j] == 2)
            {
                cout << "  ";
            }
        }
        cout << endl;
    }
}

